def prime(number):
    if number % 2 == 0:
        print("Number is not a prime")
    elif number % 2 == 0:
        print("Number is not a prime")
    else:
        print("Number is a Prime")


prime(54)
prime(5)


def operations(x, y):
    a = x + y
    b = x - y
    c = x * y
    d = x / y
    return a, b, c, d


print(operations(6, 2))